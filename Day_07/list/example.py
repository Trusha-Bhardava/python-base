# indexing example
a= ['apple','cherry',3,5,12.9,'watermelon'];
my_list=a[2]
print(my_list)

# List length
list_length = len(a)
print(list_length)

# appending to List
a.append(4)
print(a)

#removing from list
a.remove(5)
print(a)

#slicing list
slicing=a[1:3]
print(slicing)

#concatenting list
new_list = a + [0,10]
print(new_list)

# sorting example
b= [1,5,7,32,3]
b.sort()
print(b)
